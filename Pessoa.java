public class Pessoa {
   private String nome;
   private Integer idade; 

   public void setNome(String nome){
      this.nome = nome;
   }
   public String getNome(){
      return this.nome;
   }
   public Integer getIdade(){
      return this.idade;
   }
   public void setIdade(Integer idade){
      this.idade = idade;
   }
}
